from __future__ import annotations


def tool_template(param1: str = "默认值") -> str:
    """
    工具模板：演示一个最小 LangChain Tool 应该怎么写。

    用途：
    - 给 agent 提供一个可调用的能力。

    参数：
    - param1: 工具需要的输入参数。这里用一个字符串做最小示例。

    返回：
    - 返回字符串结果，模型会基于这个结果继续组织最终回答。

    注意事项：
    - 工具本质上就是普通 Python 函数，不需要继承特殊基类。
    - 函数名要尽量表达用途，方便模型理解什么时候该调用它。
    - docstring 要写清楚用途、参数和限制，agent 会参考这些描述做决策。
    - 入门阶段建议优先返回 str，这样最容易观察和调试。
    - 如果工具涉及文件、网络、数据库，最好先做输入校验，不要直接信任模型传入的参数。
    """
    return f"这是一个工具模板，收到的参数是: {param1}"


def read_todo_list() -> str:
    """
    示例工具：返回一份固定的待办事项列表。

    用途：
    - 演示“不带参数”的工具应该怎么写。

    返回：
    - 返回多行文本，模拟一个简单的数据源。

    注意事项：
    - 没有参数的工具也建议写清楚用途，避免模型误用。
    - 如果以后要从文件或数据库读取待办，不建议直接改函数名，保持工具名稳定更利于维护。
    - 返回内容尽量简洁、结构清晰，方便模型二次总结。
    """
    return "1. 学习 LangChain\n2. 理解 Tool 调用\n3. 练习做自己的 agent"


def get_weather_example(city: str) -> str:
    """
    示例工具：根据城市名称返回一条模拟天气信息。

    用途：
    - 演示“带参数”的工具应该怎么写。

    参数：
    - city: 城市名称，例如 Shanghai、Beijing。

    返回：
    - 返回一段字符串，表示工具查询到的天气结果。

    注意事项：
    - 这个函数现在只是教学示例，没有真的请求天气 API。
    - 如果你后面接真实天气服务，建议把“请求外部接口”和“结果格式化”拆开写，便于测试。
    - 参数命名要贴近业务含义，像 city 这种名字比 param1 更容易让模型正确传参。
    - 如果外部接口可能失败，记得返回明确的错误信息，而不是直接抛给用户一串异常。
    """
    return f"{city} 今天天气晴，25 度。这是一条示例返回结果。"
